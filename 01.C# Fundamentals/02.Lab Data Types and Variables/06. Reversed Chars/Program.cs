using System;

namespace _06._Reversed_Chars
{
    class Program
    {
        static void Main(string[] args)
        {
            string char1 = Console.ReadLine();
            string char2 = Console.ReadLine();
            string char3 = Console.ReadLine();
            Console.Write($"{char3} ");
            Console.Write($"{char2} ");
            Console.Write($"{char1}");
        }
    }
}
